// 1. Variable declaration using var, let, and const
var a = 10; // var variable declaration
let b = 20; // let variable declaration
const c = a + b; // const variable declaration and initialization
console.log('a + b = ' + c); // Output: a + b = 30

// 2. Simple message log in console
console.log('complete!');

// 3. Function to add two numbers
function addNumbers(x, y) {
    return x + y;
}
console.log(addNumbers(5, 10)); // Output: 15

// 4. Array declaration and accessing elements
let fruits = ['Apple', 'Banana', 'Mango'];
console.log(fruits[0]); // Output: Apple

// 5. Object declaration and accessing properties
let person = {
    name: 'Abdur Rahman',
    age: 30
};
console.log(person.name); // Output: Abdur Rahman
